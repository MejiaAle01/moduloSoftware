# Operadores Aritméticos

# 1: Suma
print("Suma: ", 5 + 10);

# Alternativas
print("Suma N Números: ", 5 + 10 + 15);

# 2: Resta
print("Resta: ", 5 - 15);

# 3: Multiplicación
print("Multiplicación: ", 3 * 5);

# 4: División
print("División: ", 15 / 5);

# 5: Potencia
print("Potencia: ", 2 ** 3);

# 6: MODULO
print("Modulo: ", 10 % 3);